    //根据一个文件名，获取其后缀名，如果没有后缀名，那么返回""
    public static String getExtension(String filename) {

        if (filename == null) {
            return "";
        }

        int dotIndex = filename.lastIndexOf(".");
        if (dotIndex == -1) {
            return "";
        } else {
            return filename.substring(dotIndex);
        }
    }

